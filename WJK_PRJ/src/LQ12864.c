/***************************************************/ 
/*函数名称：显示一位数组曲线                       */
/*功能描述：array为数组 y_data_max为数据最大值
 usRowNum usColumnNum 为输出行和列                 */
/*输出参数：无                                     */
/*返回值 无                                        */
/***************************************************/  
 
 
  
 void LCD12864_Array (unsigned char *array,unsigned int y_data_max, unsigned int usRowNum, unsigned int usColumnNum)
{

    unsigned char ucData,multiple;
    char i,j,k,m,n;
    char usRowTmp;
    multiple=1+y_data_max/Max_Row;

    m = usRowNum >> 3;   //计算图片行数以8位为一组完整的组数
    n = usRowNum % 8;    //计算分完组后剩下的行数
    
    for(i = 0; i < m; i++) //完整组行扫描
    {
        LCD12864_Set_Pos(0,(unsigned char)i);
        usRowTmp = i << 3;    //计算当前所在行的下标                  
        for(j = 0; j < usColumnNum; j++) //列扫描        
        {
            ucData = 0;
            for(k = 0; k < 8; k++) //在i组中对这8行扫描
            {
                ucData = ucData >> 1;
                if((array[j]/multiple)==64-usRowTmp-k)
                //(pucTable + (usRowTmp + k) * usColumnNum)[j] == 1)
                {
                    ucData = ucData | 0x80;
                }
                
            }
            
            LCD12864_WrDat(ucData);
        }
    }
    
    LCD12864_Set_Pos(0,(unsigned char)i); //设置剩下的行显示的起始坐标
    usRowTmp = i << 3;       //计算当前所在行的下标                  
    for(j = 0; j < usColumnNum; j++) //列扫描        
    {
        ucData = 0;
        for(k = 0; k < n; k++) //对剩下的行扫描
        {
            ucData = ucData >> 1;
            if((array[j]/multiple)==64-usRowTmp-k)
            //(pucTable + (usRowTmp + k) * usColumnNum)[j] == 1)
            {
                ucData = ucData | 0x80;
            }
            
        }
        ucData = ucData >> (8 - n);
        LCD12864_WrDat(ucData);
    }

    return;
}
                       